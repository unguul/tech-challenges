<?php

namespace IWD\JOBINTERVIEW\Survey\Question\Answer;

class NumericAnswer implements Answer
{
    /**
     * @var int
     */
    private $value;

    public function __construct(int $value)
    {
        $this->value = $value;
    }

    /**
     * @return int
     */
    public function getValue()
    {
        return $this->value;
    }
}